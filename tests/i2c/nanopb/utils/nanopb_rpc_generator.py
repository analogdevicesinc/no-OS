#!/usr/bin/env python3
"""
nanopb RPC Generator - Generates client/server stubs from protobuf service definitions

Usage:
    python nanopb_rpc_generator.py capi.proto --output capi_rpc

Generates:
    - capi_rpc.h    (method IDs, client/server function declarations)
    - capi_rpc.c    (client stubs and server dispatcher)
"""

import argparse
import re
import sys
from pathlib import Path
from dataclasses import dataclass
from typing import List


@dataclass
class RpcMethod:
    name: str
    request_type: str
    response_type: str
    is_server_streaming: bool = False
    is_client_streaming: bool = False


@dataclass
class Service:
    name: str
    methods: List[RpcMethod]


def find_matching_brace(content: str, start: int) -> int:
    """Find the matching closing brace for an opening brace at position start."""
    depth = 1
    i = start + 1
    while i < len(content) and depth > 0:
        if content[i] == '{':
            depth += 1
        elif content[i] == '}':
            depth -= 1
        i += 1
    return i


def parse_proto(proto_path: str) -> List[Service]:
    """Parse .proto file and extract service definitions."""
    with open(proto_path, 'r') as f:
        content = f.read()

    # Remove comments
    content = re.sub(r'//.*$', '', content, flags=re.MULTILINE)
    content = re.sub(r'/\*.*?\*/', '', content, flags=re.DOTALL)

    # Extract package name
    package_match = re.search(r'package\s+(\w+)\s*;', content)
    package = package_match.group(1) if package_match else ''

    services = []

    # Find all service definitions (handle nested braces)
    service_start_pattern = r'service\s+(\w+)\s*\{'
    for service_match in re.finditer(service_start_pattern, content):
        service_name = service_match.group(1)
        brace_start = service_match.end() - 1
        brace_end = find_matching_brace(content, brace_start)
        service_body = content[brace_start + 1:brace_end - 1]

        methods = []

        # Find all rpc definitions within service
        rpc_pattern = r'rpc\s+(\w+)\s*\(\s*(stream\s+)?(\w+)\s*\)\s*returns\s*\(\s*(stream\s+)?(\w+)\s*\)'
        for rpc_match in re.finditer(rpc_pattern, service_body):
            method_name = rpc_match.group(1)
            client_stream = rpc_match.group(2) is not None
            request_type = rpc_match.group(3)
            server_stream = rpc_match.group(4) is not None
            response_type = rpc_match.group(5)

            # Prefix with package name for nanopb
            if package:
                request_type = f"{package}_{request_type}"
                response_type = f"{package}_{response_type}"

            methods.append(RpcMethod(
                name=method_name,
                request_type=request_type,
                response_type=response_type,
                is_server_streaming=server_stream,
                is_client_streaming=client_stream,
            ))

        services.append(Service(name=service_name, methods=methods))

    return services


def generate_header(services: List[Service], proto_name: str) -> str:
    """Generate the .h file content."""
    guard = f"{proto_name.upper()}_RPC_H"
    lines = [
        f"/* Auto-generated by nanopb_rpc_generator.py */",
        f"#ifndef {guard}",
        f"#define {guard}",
        "",
        "#include <stdbool.h>",
        "#include <stdint.h>",
        "#include \"transport/transport.h\"",
        f"#include \"{proto_name}.pb.h\"",
        "",
    ]

    for service in services:
        # Method ID enum
        lines.append(f"/* {service.name} method IDs */")
        lines.append(f"typedef enum {{")
        lines.append(f"    {service.name.upper()}_METHOD_UNKNOWN = 0,")
        for i, method in enumerate(service.methods, start=1):
            lines.append(f"    {service.name.upper()}_METHOD_{method.name.upper()} = {i},")
        lines.append(f"}} {service.name}_MethodId;")
        lines.append("")

        # Forward declare stub for function signatures
        lines.append(f"/* Forward declaration */")
        lines.append(f"typedef struct {service.name}_Stub {service.name}_Stub;")
        lines.append("")

        # Client stub declarations
        lines.append(f"/* {service.name} client stubs */")
        for method in service.methods:
            if method.is_client_streaming:
                lines.append(f"/* TODO: {method.name} - client streaming not yet supported */")
            elif method.is_server_streaming:
                # Server streaming - return reader struct
                lines.append(f"/* Server streaming: {method.name} */")
                lines.append(f"typedef struct {service.name}_{method.name}_Reader {service.name}_{method.name}_Reader;")
                lines.append(f"typedef bool (*{service.name}_{method.name}_Read_Fn)(")
                lines.append(f"    {service.name}_{method.name}_Reader *reader, {method.response_type} *response);")
                lines.append(f"typedef bool (*{service.name}_{method.name}_Destroy_Fn)(")
                lines.append(f"    {service.name}_{method.name}_Reader *reader);")
                lines.append(f"")
                lines.append(f"struct {service.name}_{method.name}_Reader {{")
                lines.append(f"    transport_conn_t *conn;")
                lines.append(f"    {service.name}_{method.name}_Read_Fn read;")
                lines.append(f"    {service.name}_{method.name}_Destroy_Fn destroy;")
                lines.append(f"}};")
                lines.append(f"")
                lines.append(f"{service.name}_{method.name}_Reader* {service.name}_{method.name}(")
                lines.append(f"    {service.name}_Stub *stub,")
                lines.append(f"    const {method.request_type} *request);")
            else:
                lines.append(f"bool {service.name}_{method.name}(")
                lines.append(f"    {service.name}_Stub *stub,")
                lines.append(f"    const {method.request_type} *request,")
                lines.append(f"    {method.response_type} *response);")
            lines.append("")

        # Server handler callback type
        lines.append(f"/* {service.name} server handler callbacks */")

        # Server streaming handler types
        for method in service.methods:
            if method.is_server_streaming and not method.is_client_streaming:
                # Server streaming write struct
                lines.append(f"/* Server streaming writer for {method.name} */")
                lines.append(f"typedef struct {service.name}_{method.name}_Writer {service.name}_{method.name}_Writer;")
                lines.append(f"typedef bool (*{service.name}_{method.name}_Write_Fn)(")
                lines.append(f"    {service.name}_{method.name}_Writer *writer, {method.response_type} *response);")
                lines.append(f"typedef bool (*{service.name}_{method.name}_Writer_Destroy_Fn)(")
                lines.append(f"    {service.name}_{method.name}_Writer *writer);")
                lines.append(f"")
                lines.append(f"struct {service.name}_{method.name}_Writer {{")
                lines.append(f"    transport_conn_t *conn;")
                lines.append(f"    {service.name}_{method.name}_Write_Fn write;")
                lines.append(f"    {service.name}_{method.name}_Writer_Destroy_Fn Destroy;")
                lines.append(f"}};")
                lines.append(f"")
                lines.append(f"typedef bool (*{service.name}_{method.name}_Handler)(")
                lines.append(f"    const {method.request_type} *request,")
                lines.append(f"    {service.name}_{method.name}_Writer *writer,")
                lines.append(f"    void *user_data);")
                lines.append("")

        # Non-streaming handlers
        for method in service.methods:
            if not method.is_server_streaming and not method.is_client_streaming:
                lines.append(f"typedef bool (*{service.name}_{method.name}_Handler)(")
                lines.append(f"    const {method.request_type} *request,")
                lines.append(f"    {method.response_type} *response,")
                lines.append(f"    void *user_data);")
                lines.append("")

        # Server handler struct
        lines.append(f"/* {service.name} server handlers */")
        lines.append(f"typedef struct {{")
        for method in service.methods:
            if method.is_client_streaming:
                continue
            else:
                lines.append(f"    {service.name}_{method.name}_Handler {method.name};")
        lines.append(f"    void *user_data;")
        lines.append(f"}} {service.name}_Server;")
        lines.append("")

        # Server dispatch function
        lines.append(f"/* Dispatch incoming request to appropriate handler */")
        lines.append(f"bool {service.name}_Server_HandleRequest(")
        lines.append(f"    transport_conn_t *conn,")
        lines.append(f"    const {service.name}_Server *server);")
        lines.append("")

        # Client Stub struct
        lines.append(f"/* {service.name} client stub */")
        lines.append(f"struct {service.name}_Stub {{")
        lines.append(f"    transport_conn_t *channel;")
        for method in service.methods:
            if method.is_client_streaming:
                continue
            elif method.is_server_streaming:
                lines.append(f"    {service.name}_{method.name}_Reader* (*{method.name})({service.name}_Stub *stub, const {method.request_type} *request);")
            else:
                lines.append(f"    bool (*{method.name})({service.name}_Stub *stub, const {method.request_type} *request, {method.response_type} *response);")
        lines.append(f"}};")
        lines.append("")

        # NewStub / DeleteStub declarations
        lines.append(f"/* Create and destroy client stub */")
        lines.append(f"{service.name}_Stub* {service.name}_NewStub(transport_conn_t *channel);")
        lines.append(f"void {service.name}_DeleteStub({service.name}_Stub *stub);")
        lines.append("")

    lines.append(f"#endif /* {guard} */")
    return "\n".join(lines)


def generate_source(services: List[Service], proto_name: str, output_name: str) -> str:
    """Generate the .c file content."""
    lines = [
        f"/* Auto-generated by nanopb_rpc_generator.py */",
        f"#include \"{output_name}.h\"",
        "#include <pb_encode.h>",
        "#include <pb_decode.h>",
        "#include <stdlib.h>",
        "",
    ]

    for service in services:
        # Client stubs - non-streaming
        lines.append(f"/* {service.name} client stubs */")
        lines.append("")

        for method in service.methods:
            if method.is_client_streaming:
                continue
            elif method.is_server_streaming:
                # Server streaming client
                lines.append(f"/* Server streaming client for {method.name} */")
                lines.append(f"static bool {service.name}_{method.name}_read(")
                lines.append(f"    {service.name}_{method.name}_Reader *reader, {method.response_type} *response)")
                lines.append(f"{{")
                lines.append(f"    return transport_receive_message(reader->conn, {method.response_type}_fields, response);")
                lines.append(f"}}")
                lines.append(f"")
                lines.append(f"static bool {service.name}_{method.name}_destroy(")
                lines.append(f"    {service.name}_{method.name}_Reader *reader)")
                lines.append(f"{{")
                lines.append(f"    if (reader) {{")
                lines.append(f"        free(reader);")
                lines.append(f"    }}")
                lines.append(f"    return true;")
                lines.append(f"}}")
                lines.append(f"")
                lines.append(f"{service.name}_{method.name}_Reader* {service.name}_{method.name}(")
                lines.append(f"    {service.name}_Stub *stub,")
                lines.append(f"    const {method.request_type} *request)")
                lines.append(f"{{")
                lines.append(f"    /* Send method ID */")
                lines.append(f"    uint8_t method_id = {service.name.upper()}_METHOD_{method.name.upper()};")
                lines.append(f"    pb_ostream_t ostream = transport_get_ostream(stub->channel);")
                lines.append(f"    if (!pb_encode_varint(&ostream, method_id)) {{")
                lines.append(f"        return NULL;")
                lines.append(f"    }}")
                lines.append(f"")
                lines.append(f"    /* Send request */")
                lines.append(f"    if (!transport_send_message(stub->channel, {method.request_type}_fields, request)) {{")
                lines.append(f"        return NULL;")
                lines.append(f"    }}")
                lines.append(f"")
                lines.append(f"    /* Create reader */")
                lines.append(f"    {service.name}_{method.name}_Reader *reader = malloc(sizeof({service.name}_{method.name}_Reader));")
                lines.append(f"    if (reader) {{")
                lines.append(f"        reader->conn = stub->channel;")
                lines.append(f"        reader->read = {service.name}_{method.name}_read;")
                lines.append(f"        reader->destroy = {service.name}_{method.name}_destroy;")
                lines.append(f"    }}")
                lines.append(f"    return reader;")
                lines.append(f"}}")
                lines.append(f"")
            else:
                # Non-streaming request/response
                lines.append(f"bool {service.name}_{method.name}(")
                lines.append(f"    {service.name}_Stub *stub,")
                lines.append(f"    const {method.request_type} *request,")
                lines.append(f"    {method.response_type} *response)")
                lines.append(f"{{")
                lines.append(f"    /* Send method ID */")
                lines.append(f"    uint8_t method_id = {service.name.upper()}_METHOD_{method.name.upper()};")
                lines.append(f"    pb_ostream_t ostream = transport_get_ostream(stub->channel);")
                lines.append(f"    if (!pb_encode_varint(&ostream, method_id)) {{")
                lines.append(f"        return false;")
                lines.append(f"    }}")
                lines.append(f"")
                lines.append(f"    /* Send request */")
                lines.append(f"    if (!transport_send_message(stub->channel, {method.request_type}_fields, request)) {{")
                lines.append(f"        return false;")
                lines.append(f"    }}")
                lines.append(f"")
                lines.append(f"    /* Receive response */")
                lines.append(f"    return transport_receive_message(stub->channel, {method.response_type}_fields, response);")
                lines.append(f"}}")
                lines.append(f"")

        # Server streaming write functions
        for method in service.methods:
            if method.is_server_streaming and not method.is_client_streaming:
                lines.append(f"/* Server streaming writer for {method.name} */")
                lines.append(f"static bool {service.name}_{method.name}_write(")
                lines.append(f"    {service.name}_{method.name}_Writer *writer, {method.response_type} *response)")
                lines.append(f"{{")
                lines.append(f"    return transport_send_message(writer->conn, {method.response_type}_fields, response);")
                lines.append(f"}}")
                lines.append(f"")
                lines.append(f"static bool {service.name}_{method.name}_writer_destroy(")
                lines.append(f"    {service.name}_{method.name}_Writer *writer)")
                lines.append(f"{{")
                lines.append(f"    if (writer) {{")
                lines.append(f"        free(writer);")
                lines.append(f"    }}")
                lines.append(f"    return true;")
                lines.append(f"}}")
                lines.append(f"")

        # Server dispatcher
        lines.append(f"/* {service.name} server dispatcher */")
        lines.append("")
        lines.append(f"bool {service.name}_Server_HandleRequest(")
        lines.append(f"    transport_conn_t *conn,")
        lines.append(f"    const {service.name}_Server *server)")
        lines.append(f"{{")
        lines.append(f"    /* Read method ID */")
        lines.append(f"    pb_istream_t istream = transport_get_istream(conn);")
        lines.append(f"    uint64_t method_id;")
        lines.append(f"    if (!pb_decode_varint(&istream, &method_id)) {{")
        lines.append(f"        return false;")
        lines.append(f"    }}")
        lines.append(f"")
        lines.append(f"    switch (({service.name}_MethodId)method_id) {{")

        for method in service.methods:
            if method.is_client_streaming:
                continue

            lines.append(f"    case {service.name.upper()}_METHOD_{method.name.upper()}: {{")
            lines.append(f"        {method.request_type} request = {method.request_type}_init_zero;")
            lines.append(f"")
            lines.append(f"        if (!transport_receive_message(conn, {method.request_type}_fields, &request)) {{")
            lines.append(f"            return false;")
            lines.append(f"        }}")
            lines.append(f"")

            if method.is_server_streaming:
                # Server streaming - allocate writer on heap
                lines.append(f"        /* Server streaming */")
                lines.append(f"        /* IMPORTANT: User handler must free writer when done */")
                lines.append(f"        {service.name}_{method.name}_Writer *writer = malloc(sizeof({service.name}_{method.name}_Writer));")
                lines.append(f"        if (!writer) {{")
                lines.append(f"            return false;")
                lines.append(f"        }}")
                lines.append(f"")
                lines.append(f"        writer->conn = conn;")
                lines.append(f"        writer->write = {service.name}_{method.name}_write;")
                lines.append(f"        writer->Destroy = {service.name}_{method.name}_writer_destroy;")
                lines.append(f"")
                lines.append(f"        if (server->{method.name}) {{")
                lines.append(f"            if (!server->{method.name}(&request, writer, server->user_data)) {{")
                lines.append(f"                free(writer);")
                lines.append(f"                return false;")
                lines.append(f"            }}")
                lines.append(f"        }}")
                lines.append(f"")
                lines.append(f"        return true;")
            else:
                # Non-streaming request/response
                lines.append(f"        {method.response_type} response = {method.response_type}_init_zero;")
                lines.append(f"")
                lines.append(f"        if (server->{method.name}) {{")
                lines.append(f"            if (!server->{method.name}(&request, &response, server->user_data)) {{")
                lines.append(f"                return false;")
                lines.append(f"            }}")
                lines.append(f"        }}")
                lines.append(f"")
                lines.append(f"        return transport_send_message(conn, {method.response_type}_fields, &response);")

            lines.append(f"    }}")

        lines.append(f"    default:")
        lines.append(f"        return false;")
        lines.append(f"    }}")
        lines.append(f"}}")
        lines.append("")

        # NewStub implementation
        lines.append(f"/* {service.name} stub creation */")
        lines.append("")
        lines.append(f"{service.name}_Stub* {service.name}_NewStub(transport_conn_t *channel)")
        lines.append(f"{{")
        lines.append(f"    {service.name}_Stub *stub = malloc(sizeof({service.name}_Stub));")
        lines.append(f"    if (!stub) {{")
        lines.append(f"        return NULL;")
        lines.append(f"    }}")
        lines.append(f"")
        lines.append(f"    stub->channel = channel;")
        for method in service.methods:
            if method.is_client_streaming:
                continue
            elif method.is_server_streaming:
                lines.append(f"    stub->{method.name} = &{service.name}_{method.name};")
            else:
                lines.append(f"    stub->{method.name} = &{service.name}_{method.name};")
        lines.append(f"")
        lines.append(f"    return stub;")
        lines.append(f"}}")
        lines.append("")

        # DeleteStub implementation
        lines.append(f"void {service.name}_DeleteStub({service.name}_Stub *stub)")
        lines.append(f"{{")
        lines.append(f"    if (stub) {{")
        lines.append(f"        free(stub);")
        lines.append(f"    }}")
        lines.append(f"}}")
        lines.append("")

    return "\n".join(lines)


def main():
    parser = argparse.ArgumentParser(description='Generate nanopb RPC stubs from proto service definitions')
    parser.add_argument('proto', help='Input .proto file')
    parser.add_argument('--output', '-o', default=None, help='Output base name (default: <proto>_rpc)')
    args = parser.parse_args()

    proto_path = Path(args.proto)
    if not proto_path.exists():
        print(f"Error: {proto_path} not found", file=sys.stderr)
        return 1

    proto_name = proto_path.stem
    output_name = args.output or f"{proto_name}_rpc"

    services = parse_proto(str(proto_path))

    if not services:
        print(f"No services found in {proto_path}", file=sys.stderr)
        return 1

    print(f"Found {len(services)} service(s):")
    for svc in services:
        print(f"  - {svc.name}: {len(svc.methods)} methods")

    # Generate files
    header = generate_header(services, proto_name)
    source = generate_source(services, proto_name, output_name)

    output_dir = proto_path.parent
    header_path = output_dir / f"{output_name}.h"
    source_path = output_dir / f"{output_name}.c"

    with open(header_path, 'w') as f:
        f.write(header)
    print(f"Generated: {header_path}")

    with open(source_path, 'w') as f:
        f.write(source)
    print(f"Generated: {source_path}")

    return 0


if __name__ == '__main__':
    sys.exit(main())
