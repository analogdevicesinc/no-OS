static int lscc_putc(char c, FILE *file)
{

#ifdef LSCC_STDIO_UART_APB
	if(g_stdio_uart) {
		//Initialize the uart driver if it is the first time this function is called
		int ret = EOF;
		ret = uart_putc(g_stdio_uart, c);
		if (c == '\n' && ret == 0)
			ret = uart_putc(g_stdio_uart, '\r');
		return ret;
	}
	else
		return EOF;
#endif

}

